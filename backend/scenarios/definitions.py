"""
Сценарии для AI Arena — системные промпты на русском языке.
Базовые нейтральные промпты (base_neutral) скрыты от пользователя.
В to_dict() передаются только краткие подсказки-описания.
"""

from __future__ import annotations

import random
from dataclasses import dataclass


@dataclass
class ScenarioDef:
    id: str
    name: str
    description: str
    max_turns: int
    secrets: list[str]
    agent_1_label: str
    agent_2_label: str
    agent_1_roles: list[str]
    agent_2_roles: list[str]
    agent_1_base_neutral: str      # скрыт от пользователя, всегда отправляется LLM
    agent_2_base_neutral: str      # скрыт от пользователя, всегда отправляется LLM
    agent_1_user_default: str      # краткая подсказка в интерфейсе
    agent_2_user_default: str      # краткая подсказка в интерфейсе
    judge_template: str            # скрыт от пользователя

    @property
    def agent_1_template(self) -> str:
        return self.agent_1_user_default

    @property
    def agent_2_template(self) -> str:
        return self.agent_2_user_default

    def pick_secret(self) -> str:
        return random.choice(self.secrets)

    def pick_agent_1_role(self) -> str:
        return random.choice(self.agent_1_roles)

    def pick_agent_2_role(self) -> str:
        return random.choice(self.agent_2_roles)

    @staticmethod
    def _assemble(base_template: str, user_prompt: str, secret: str = "",
                  role: str = "", max_turns: int = 0, current_turn: int = 0) -> str:
        """Собирает финальный системный промпт: база + пользовательские инструкции."""
        compiled = base_template
        compiled = compiled.replace("{{secret_data}}", secret)
        compiled = compiled.replace("{{secret}}", secret)
        compiled = compiled.replace("{{role}}", role)
        compiled = compiled.replace("{{max_turns}}", str(max_turns))
        compiled = compiled.replace("{{current_turn}}", str(current_turn))

        clean = (user_prompt or "").strip()
        if not clean:
            clean = (
                "Действуй спокойно, профессионально и нейтрально. "
                "Не выдавай информацию без необходимости. Оставайся в роли."
            )

        return f"{compiled}\n\n[ПОЛЬЗОВАТЕЛЬСКИЕ ИНСТРУКЦИИ]:\n{clean}"

    def build_agent_1_prompt(self, secret: str, max_turns: int, custom_prompt: str = "") -> str:
        role = self.pick_agent_1_role()
        return self._assemble(
            self.agent_1_base_neutral, custom_prompt,
            secret=secret, role=role, max_turns=max_turns,
        )

    def build_agent_2_prompt(self, max_turns: int, current_turn: int, custom_prompt: str = "") -> str:
        role = self.pick_agent_2_role()
        return self._assemble(
            self.agent_2_base_neutral, custom_prompt,
            role=role, max_turns=max_turns, current_turn=current_turn,
        )

    def build_judge_prompt(self, secret: str, max_turns: int, custom_prompt: str = "") -> str:
        template = custom_prompt.strip() or self.judge_template
        return template.format(secret=secret, max_turns=max_turns)

    def to_dict(self) -> dict:
        """Публичный API — системные промпты НЕ передаются."""
        return {
            "id": self.id,
            "name": self.name,
            "description": self.description,
            "max_turns": self.max_turns,
            "agent_1_label": self.agent_1_label,
            "agent_2_label": self.agent_2_label,
            "agent_1_roles": self.agent_1_roles,
            "agent_2_roles": self.agent_2_roles,
            "agent_1_prompt_hint": self.agent_1_user_default,
            "agent_2_prompt_hint": self.agent_2_user_default,
            "judge_prompt_hint": "Судья оценивает расшифровку диалога и выносит вердикт по критериям сценария.",
        }


